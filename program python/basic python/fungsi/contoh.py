import math
#fungsi lengkap dengan sederhana
def main(a , b) :
    return a + b

print(main(4 , 2))

#fungsi sederhana dengan volume kubus

def kubus(a1) :
    return a1**3

print(kubus(9))
# fungsi sederhana dengan volume balok 
def balok(a2 , b2 , c2) :
    return a2*b2*c2

print(balok(3 , 4 , 2))

# fungsi sederhana dengan volume tabung
def tabung(a3 , b3):
    return math.pi**2*a3*b3

print(tabung(4 , 6))